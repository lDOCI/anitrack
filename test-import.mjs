for (const exp of expectations) {
  const parsed = parseLines(exp.input)
  if (!parsed.length) {
    problems.push(`  ⚠️  "${exp.input}" → НЕ РАСПАРСИЛОСЬ`)
    continue
  }
  const p = parsed[0]
  const seasonOk = p.season === (exp.season ?? null)
  const hadPartOk = exp.hadPart === undefined || p.hadPart === exp.hadPart
  if (!seasonOk || !hadPartOk) {
    problems.push(`  ⚠️  "${exp.input}" → title="${p.title}" season=${p.season} hadPart=${p.hadPart}  (ожидалось: season=${exp.season ?? null}${exp.hadPart !== undefined ? ` hadPart=${exp.hadPart}` : ''})`)
  }
}

